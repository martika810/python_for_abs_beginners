# Write a program to input week number and print week day
# Example: 1 -> Monday
#          2 -> Tuesday
week_number = int(input('Please enter a week number: '))
if(week_number == 1):
    print('Monday')
elif(week_number == 2):
    print('Tuesday')
elif(week_number ==3):
    print('Wednesday')
elif(week_number == 4):
    print('Thursday')
elif(week_number == 5):
    print('Friday')
elif(week_number == 6):
    print('Saturday')
elif(week_number == 7):
    print('Sunday')
else:
    print('None')
